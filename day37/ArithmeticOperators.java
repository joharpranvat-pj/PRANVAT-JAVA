package com.pranvat.day37;

public class ArithmeticOperators {

    public static void main(String[] args) {
        int a = 8, b = 4;   // two integer variables

        System.out.print("Addition Operator (+): ");
        System.out.println(a + b);   // 8 + 4 = 12

        System.out.print("Subtraction Operator (-): ");
        System.out.println(a - b);   // 8 - 4 = 4

        System.out.print("Multiplication Operator (*): ");
        System.out.println(a * b);   // 8 * 4 = 32

        System.out.print("Division Operator (/): ");
        System.out.println(a / b);   // 8 / 4 = 2

        System.out.print("Modulus Operator (%): ");
        System.out.println(a % b);   // 8 % 4 = 0 (remainder)
    }
}


